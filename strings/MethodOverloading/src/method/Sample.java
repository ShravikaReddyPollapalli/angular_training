package method;

public class Sample {
public void add(int a,int b)
{
	System.out.println(a+b);
}
public static void add(int a,double b)
{
	System.out.println(a+b);
}
public void add(int a,int b,int c)
{
	System.out.println(a+b+c);
}
protected void add(int a,double b,double c)
{
	System.out.println(a+b+c);
}
}
